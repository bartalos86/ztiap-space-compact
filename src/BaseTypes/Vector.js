export class Vector2D{
    constructor(x, y) {
        this.x = x;
        this.y = y;
    }

    setPosition(x, y) {
        this.x = x;
        this.y = y;
    }

    setX(x) {
        this.x = x;
    }

    setY(y) {
        this.y = y;
    }

    getX() {
       return this.x
    }

    getY() {
       return this.y
    }
}